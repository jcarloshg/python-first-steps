import math

print(round(3.14))  # 3
print(round(3.7))  # 4
print(round(3.5))  # 4
print(abs(-77))  # 77
print(abs(77))  # 77

print(math.ceil(3.14))  # 4
print(math.floor(3.7))  # 3
print(math.sqrt(16))  # 4.0
print(math.pow(2, 3))  # 8.0
print(math.pi)  # 3.141592653589793
print(math.e)  # 2.718281828459045
print(math.isnan(0.0))  # False
